import os
from typing import List, Dict
from dotenv import load_dotenv

# Point RAGAS to OpenAI API
os.environ["OPENAI_API_KEY"] = os.getenv("OPENAI_API_KEY", "")

from datasets import Dataset
from ragas import evaluate
from ragas.metrics import (
    Faithfulness,
    AnswerRelevancy,
    ContextPrecision,
    ContextRecall,
)
from ragas.llms import LangchainLLMWrapper
from ragas.embeddings import LangchainEmbeddingsWrapper
from langchain_openai import ChatOpenAI, OpenAIEmbeddings

load_dotenv()


def get_ragas_config():
    """
    Configure RAGAS to use OpenAI models explicitly.
    RAGAS 0.4.x requires explicit LLM and embeddings configuration.
    """
    llm = LangchainLLMWrapper(
        ChatOpenAI(
            model="gpt-4o-mini",
            temperature=0,
            api_key=os.getenv("OPENAI_API_KEY"),
        )
    )

    embeddings = LangchainEmbeddingsWrapper(
        OpenAIEmbeddings(
            api_key=os.getenv("OPENAI_API_KEY"),
        )
    )

    return llm, embeddings


def evaluate_pipeline(
    pipeline_name: str,
    questions: List[str],
    answers: List[str],
    contexts: List[List[str]],
    ground_truths: List[str],
) -> Dict:
    """
    Evaluate a single RAG pipeline using RAGAS metrics.

    Args:
        pipeline_name: Name of the pipeline being evaluated
        questions: List of evaluation questions
        answers: List of answers generated by the pipeline
        contexts: List of lists — retrieved chunks per question
        ground_truths: List of correct reference answers

    Returns:
        Dict with pipeline_name and all 4 RAGAS scores rounded to 3 decimals

    Metrics explained:
        Faithfulness: Does the answer contain ONLY info from retrieved context?
                      Catches hallucination. Higher is better.
        Answer Relevancy: Does the answer actually address the question asked?
                          Catches on-topic but useless answers.
        Context Precision: Are the retrieved chunks actually relevant to the question?
                           Measures retrieval quality.
        Context Recall: Did the retriever find ALL relevant information?
                        Requires ground truth to calculate.
    """
    print(f"\nEvaluating {pipeline_name}...")
    print(f"Running RAGAS on {len(questions)} questions — this takes 2-3 minutes...")

    # Build HuggingFace Dataset — RAGAS requires this exact format
    data = {
        "question": questions,
        "answer": answers,
        "contexts": contexts,       # list of lists — chunks retrieved per question
        "ground_truth": ground_truths,
    }

    dataset = Dataset.from_dict(data)

    # Configure RAGAS with explicit LLM and embeddings
    llm, embeddings = get_ragas_config()

    # Initialize metrics with explicit config
    metrics = [
        Faithfulness(llm=llm),
        AnswerRelevancy(llm=llm, embeddings=embeddings),
        ContextPrecision(llm=llm),
        ContextRecall(llm=llm),
    ]

    # Run evaluation
    results = evaluate(
        dataset=dataset,
        metrics=metrics,
    )

    # Convert to pandas for easy extraction
    results_df = results.to_pandas()

    # Calculate mean score for each metric across all questions
    faithfulness = round(float(results_df["faithfulness"].mean()), 3)
    answer_relevancy = round(float(results_df["answer_relevancy"].mean()), 3)
    context_precision = round(float(results_df["context_precision"].mean()), 3)
    context_recall = round(float(results_df["context_recall"].mean()), 3)

    print(f"\n{pipeline_name} Results:")
    print(f"  Faithfulness:      {faithfulness}")
    print(f"  Answer Relevancy:  {answer_relevancy}")
    print(f"  Context Precision: {context_precision}")
    print(f"  Context Recall:    {context_recall}")

    return {
        "pipeline_name": pipeline_name,
        "faithfulness": faithfulness,
        "answer_relevancy": answer_relevancy,
        "context_precision": context_precision,
        "context_recall": context_recall,
    }


def compare_pipelines(all_results: Dict[str, Dict]) -> str:
    """
    Compare RAGAS scores across all pipelines and identify the winner
    for each metric.

    Args:
        all_results: Dict of {pipeline_name: scores_dict}
                     e.g. {"Pipeline 1": {"faithfulness": 0.85, ...}}

    Returns:
        Formatted summary string showing scores and winner per metric
    """
    metrics = [
        "faithfulness",
        "answer_relevancy",
        "context_precision",
        "context_recall",
    ]

    lines = []
    lines.append("\n" + "=" * 60)
    lines.append("RAGAS EVALUATION RESULTS — ALL PIPELINES")
    lines.append("=" * 60)

    # Print scores table
    lines.append(f"\n{'Metric':<25} " + " ".join(
        f"{name[:12]:<14}" for name in all_results.keys()
    ))
    lines.append("-" * 60)

    winners = {}
    for metric in metrics:
        scores = {
            name: results[metric]
            for name, results in all_results.items()
        }
        winner = max(scores, key=scores.get)
        winners[metric] = (winner, scores[winner])

        row = f"{metric:<25} " + " ".join(
            f"{score:<14.3f}" for score in scores.values()
        )
        lines.append(row)

    # Print winners
    lines.append("\n" + "=" * 60)
    lines.append("WINNERS PER METRIC")
    lines.append("=" * 60)

    for metric, (winner, score) in winners.items():
        lines.append(f"  {metric:<25} → {winner} ({score})")

    # Overall winner — average across all metrics
    overall_scores = {
        name: round(sum(results[m] for m in metrics) / len(metrics), 3)
        for name, results in all_results.items()
    }
    overall_winner = max(overall_scores, key=overall_scores.get)

    lines.append("\n" + "=" * 60)
    lines.append("OVERALL WINNER")
    lines.append("=" * 60)
    lines.append(f"  {overall_winner} (avg score: {overall_scores[overall_winner]})")

    for name, score in overall_scores.items():
        marker = " ← WINNER" if name == overall_winner else ""
        lines.append(f"  {name:<40} avg: {score}{marker}")

    lines.append("=" * 60)

    summary = "\n".join(lines)
    return summary